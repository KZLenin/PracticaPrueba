public class Ciudadanos {
    private String nombre;
    private String apellido;
    private String cedula;
    private int edad;
    private String sexo;

    public Ciudadanos(String nombre, String apellido, String cedula, int edad, String sexo){
        this.nombre = nombre;
        this.apellido = apellido;
        this.cedula = cedula;
        this.edad = edad;
        this.sexo = sexo;
    }
    public Ciudadanos(){}


}
